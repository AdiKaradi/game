		function mixer(Arr) { //ערבוב המסיחים
			//יצירת העתק של המערך המקורי
			var ArrClone = [...Arr];
			//יצירת מערך ריק שהוא ישמור את התאים אחרי ערבול
			var mixedArr = [];
			//יצירת משתנה ששומר את אורך המערך המקורי
			var size = Arr.length;
		
			//לולאה שבודקת כל תא במערך המקורי
			for (var i = 0; i < size; i++) {
				//חיפוש אינדקס רנדומלי
				var randomIndex = Math.floor(Math.random() * ArrClone.length);
				//הכנסה של ערך התא החדש למערך המעורבב
				mixedArr[i] = ArrClone[randomIndex];
				//מוריד את התא בהעתק של המערך
				ArrClone.splice(randomIndex, 1);
			}
			//מחזיר מערך מעורבב
			return mixedArr;
		}
			
